'''
Потрібно порахувати суму чисел від a до b. 
Програма запитує два числа і виводить результат - одне число.


TODO input:
2
4
TODO output:
9
'''

a = int(input("Введіть перше число (a): "))
b = int(input("Введіть друге число (b): "))

sum = 0

for i in range(a, b + 1):
    sum  += i

print("Сума чисел від", a, "до", b, "дорівнює:", sum)
